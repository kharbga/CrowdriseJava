    private void jBtnValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnValiderActionPerformed
        if (jTxtFldTitreProb.getText() == null || jTxtFldTitreProb.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Le champ Titre est obligatoire", "", JOptionPane.WARNING_MESSAGE);
        } else if (jTextAreaDescriptionProb.getText() == null || jTextAreaDescriptionProb.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Le champ description est obligatoire", "", JOptionPane.WARNING_MESSAGE);
        } else if (jDtChooserDeadline.getDate() == null || jDtChooserDeadline.getDate().equals("")) {
            JOptionPane.showMessageDialog(this, "Le champ Deadline est obligatoire", "", JOptionPane.WARNING_MESSAGE);
        }else if (jDtChooserDeadline.getDate() != null && jDtChooserDeadline.getDate().before(new java.util.Date())) {
            JOptionPane.showMessageDialog(this, "Vous devez ins√©rer une date valide", "", JOptionPane.WARNING_MESSAGE);
        }  else {
            try {
                
                new DAO.ProblemeDao().addToMedia(num + 1, fileName, filePath);
                new DAO.ProblemeDao().uploadFile(filePath,"C:\\xampp\\htdocs\\Pidev\\web\\uploads\\" + fileName );
                
                new DAO.ProblemeDao().update(new Probleme(Integer.valueOf(jTxtFldIDProb.getText()), jTxtFldTitreProb.getText(),
                         jTextAreaDescriptionProb.getText(), jDtChooserDeadline.getDate(), String.valueOf(num+1),
                          jComboCategorie.getSelectedItem().toString()));
                 

                jFrameProblem.setLay();

                JOptionPane.showMessageDialog(this, "Votre Demande a √©t√© modifi√©e avec succ√©s !",
                        "Modification Demande", JOptionPane.INFORMATION_MESSAGE);

                this.hide();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Demande non Modifi√©e", "Modification Demande", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jBtnValiderActionPerformed

