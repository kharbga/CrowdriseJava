    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        int row = tbMembre.getSelectedRow();

        if (row == -1) {
                JOptionPane.showMessageDialog(this, "Vous devez Selectionner le Membre a activer d'abord.");
        } else {

            MembreDao md = new MembreDao();
            Membre p;
            p = md.findById((int) tbMembre.getValueAt(tbMembre.getSelectedRow(), 0));
            p.setEtat(1);
            md.updateEtat(p);
            tbMembre.setModel(new JtableListeMembre());
        }
    }//GEN-LAST:event_button1ActionPerformed

